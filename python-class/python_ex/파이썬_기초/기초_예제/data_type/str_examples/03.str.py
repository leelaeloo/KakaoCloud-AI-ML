# 문자열 연산
concatenation = "Lee" + " " + "Tae Soo"     # "Lee Tae Soo"
print(concatenation)

repetition = "Hi" * 3       # "HiHiHi"
print(repetition)

# 문자열 인덱싱
text = "Python"
first_char = text[0]        # 'P'
print(first_char)

last_char = text[-1]        # 'n'
print(last_char)

# 문자열 슬라이싱
substring = text[1:4]       # 'yth'
print(substring)